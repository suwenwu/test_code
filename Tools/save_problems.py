def format_luogu(problem):
    lines = []
    lines.append(f'# {problem["name"]}')
    lines.append('')
    lines.append(f'> 时空限制：{problem["time"]}秒 / {problem["memory"]}MB')
    lines.append('')
    
    lines.append('## 题目描述')
    lines.append('')
    lines.append(problem["content"])
    lines.append('')
    
    lines.append('## 输入格式')
    lines.append('')
    lines.append(problem["inFormat"])
    lines.append('')
    
    lines.append('## 输出格式')
    lines.append('')
    lines.append(problem["outFormat"])
    lines.append('')
    
    lines.append('## 输入输出样例')
    lines.append('')
    for i, ex in enumerate(problem["examples"]):
        if len(problem["examples"]) > 1:
            lines.append(f'### 样例 #1')
            lines.append('')
        lines.append('```input')
        lines.append(ex["in"])
        lines.append('```')
        lines.append('')
        lines.append('```output')
        lines.append(ex["out"])
        lines.append('```')
        if "desc" in ex and ex["desc"]:
            lines.append(f'\n**说明：** {ex["desc"]}')
        lines.append('')
    
    if problem["dataRange"].strip():
        lines.append('## 数据范围与提示')
        lines.append('')
        lines.append(problem["dataRange"])
        lines.append('')
    
    return '\n'.join(lines)

